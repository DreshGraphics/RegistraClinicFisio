    public String retornarUsuario() {
        switch (jcTipoUsuario.getSelectedIndex()) {
            case 1:
                return "Aluno";
            case 2:
                return "Funcion치rio";
            case 3:
                return "Paciente";
            default:
                return "Supervisor";
        }
    }

